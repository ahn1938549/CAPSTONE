void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) // 초음파 함수
{
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
	{
		SONIC_BUFFER[SONIC_FLAG] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // 초음파 값 받기 /58은 거리계산
		if(SONIC_FLAG ==1)
		{
			SONIC_FLAG = 0;

			if(SONIC_BUFFER[0] > SONIC_BUFFER[1])                                      // 만약 값이 65536을 넘어갈 경우 예외처리
			{
				SONIC_BUFFER[2] = (TIM1->ARR - SONIC_BUFFER[0] + SONIC_BUFFER[1]) / 58;
			}
			else                                                                       // 예외처리 필요없이 사용
				SONIC_BUFFER[2] = (SONIC_BUFFER[1] - SONIC_BUFFER[0]) / 58;
		}
		else
		{
			SONIC_FLAG = 1;
		}
	}
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) //USART6번 IMU 센서 값 받아오는 곳
{

	if(huart->Instance == USART6)
	{
		HAL_UART_Receive_IT(&huart6, &Rx_data, 1);  //IMU가 하나씩 던져줍니다.
		Rx_buffer[Rx_indx++] = Rx_data;
	}
}
void IMU(void)
{
	int i = 1; //전반적인 배열 순회 변수
	int j =0; // IMU의 정수 부분
	int k =1; // IMU의 소수 부분
	int index=0; // IMU 센서 값의 인덱스
	double sensor_temp;
	uint8_t temp[10]={0,};
	for(i; Rx_buffer[i]!=10; i++) //문장 전반적인 부분
	{
		for(j=0; Rx_buffer[i+j]!=46 ; j++) // .을 만나기 전까지 정수부분
		{
			temp[j] = Rx_buffer[i+j];
		}
		sensor_temp = atoi(temp); // 정수 부분 atoi 사용

		for(k = 1; Rx_buffer[i+j+k]!=44 && Rx_buffer[i+j+k]!=13; k++) //,부분 및 \n 만나기 전까지 받아오는 부분
		{
			if (temp[0] == 45) // 음수일 경우
				{
				sensor_temp = sensor_temp - pow(0.1, k) * (Rx_buffer[i+j+k] - 48);
				}
			else // 양수일 경우
				{
				sensor_temp = sensor_temp + pow(0.1, k) * (Rx_buffer[i+j+k] - 48);
				}
		}
		i = i+j+k;
		for(int temp_size=0; temp_size<sizeof(temp);temp_size++) // 받아왔던 배열 초기화
		{
			temp[temp_size] = 0;
		}
		IMU_sensor[index++] = sensor_temp; // 센서값 저장
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	char star[2]={0X2A,}; // IMU 폴링 모드
	uint8_t str[20]={0,}; // 초음파 센서 받아오는 모드





	if(htim->Instance == TIM1)
	{

		USART_CNT++;

		if(USART_CNT == 50)
		{
			if(Rx_indx ==0)
			{

				for(int i=0;i<99;i++)
				{
					Rx_buffer[i] =0X00;
				}

			}
			HAL_UART_Transmit(&huart6, star, 1, 1000);

		}
		if(USART_CNT == 100)
		{
			USART_CNT = 0;
			Rx_indx=0;
			IMU();
			HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
			HAL_UART_Transmit(&huart2, Rx_buffer,100,1000);

			sprintf(str,"SONIC %06d \n", SONIC_BUFFER[2]);
			HAL_UART_Transmit(&huart2, (uint8_t*)str,sizeof(str),1000);
		}
	}
}


